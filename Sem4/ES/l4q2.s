	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R0,[R0]
	MOV R1,#1	;POWER
	MOV R2,#10	;BASE
	MOV R5,#8
L1	AND R3,R0,#0xF ;GET THE LAST 4 BITS
	MLA R4,R1,R3,R4	;SUM(R4)=POWER(R1)*DIG(R3)+CURRENT(R4)
	MUL R1,R2	;R1=R1*10
	LSR R0,#4	;get next dig to right most
	SUBS R5,#1
	BNE L1
	LDR R0,=DST
	STR R4,[R0]
SRC DCD 0x12345678
	AREA mydata, DATA, READWRITE
DST DCD 0
	END
