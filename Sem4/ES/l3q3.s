	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=num1
	LDR R1,[R0]
	LDR R0,=num2
	LDR R2,[R0]
	MOV R3,#0
UP	CMP R2,R1
	BCC ST
	SUB R2,R1
	ADD R3,#1
	B UP
ST	LDR R0,=QR
	STR R3,[R0],#4
	STR R2,[R0]
STOP B STOP
num1 DCD 0x12345678
num2 DCD 0x23456789
	AREA mydata, DATA, READWRITE
QR DCD 0
	END