	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R1,=SRC
	LDR R2,=DST
	MOV R0,#10
up LDR R3,[R1],#4
	STR R3,[R2],#4
	SUBS R0,#1
	BNE up
SRC DCD 1,2,3,4,5,6,7,8,9,0xA
STOP B STOP
	AREA mydata, DATA, READWRITE
DST DCD 0
	END